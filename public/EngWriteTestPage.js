//array shuffle https://www.nxworld.net/tips/js-array-shuffle.html より
function shuffle(array){
    for (let i = array.length - 1; i >= 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}


//英単語リストから問題のパターンを絞る用
function Q_selecter(pattern){
    let qRange;
    switch(pattern){
        case "A1":
            qRange = 0;
            break;
        
        case "A2":
            qRange = 100;
            break;

        case "B1":
            qRange = 200;
            break;
        
        case "B2":
            qRange = 300;
            break;
        case "C1":
            qRange = 400;
            break;
        
        case "C2":
            qRange = 500;
            break;
        case "D1":
            qRange = 600;
            break;
        
        case "D2":
            qRange = 700;
            break;
        case "E1":
            qRange = 800;
            break;
        
        case "E2":
            qRange = 900;
            break;            
        default:
            qRange = 1100;
            break;
    }

    return qRange;
}
